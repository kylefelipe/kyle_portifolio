function populaSelecao(opcoes, elemento) {
  elemento.innerHTML = '';
  let itemLista = document.createElement('option');
  itemLista.value = '';
  itemLista.innerText = "Selecione!";
  elemento.appendChild(itemLista);
  for (const opcao in opcoes) {
    itemLista = document.createElement('option');
    itemLista.value = opcao;
    itemLista.innerText = opcoes[opcao];
    elemento.appendChild(itemLista);
  }
}

